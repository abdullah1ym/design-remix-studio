export const getSoundById = (id: string): ArabicSound | undefined => {
  return arabicSounds.find(s => s.id === id);
};

export const getSoundByLetter = (letter: string): ArabicSound | undefined => {
  return arabicSounds.find(s => s.letter === letter);
};

export const getSoundsByArticulationPoint = (point: string): ArabicSound[] => {
  return arabicSounds.filter(s => s.articulationPoint === point);
};

export const getSimilarSounds = (soundId: string): ArabicSound[] => {
  const sound = getSoundById(soundId);
  if (!sound) return [];
  return sound.similarSounds
    .map(letter => getSoundByLetter(letter))
    .filter((s): s is ArabicSound => s !== undefined);
};

export const getNextLevel = (currentLevel: TrainingLevel): TrainingLevel | null => {
  const currentIndex = TRAINING_LEVEL_ORDER.indexOf(currentLevel);
  if (currentIndex === -1 || currentIndex === TRAINING_LEVEL_ORDER.length - 1) {
    return null;
  }
  return TRAINING_LEVEL_ORDER[currentIndex + 1];
};

export const getPreviousLevel = (currentLevel: TrainingLevel): TrainingLevel | null => {
  const currentIndex = TRAINING_LEVEL_ORDER.indexOf(currentLevel);
  if (currentIndex <= 0) {
    return null;
  }
  return TRAINING_LEVEL_ORDER[currentIndex - 1];
};

export const getLevelNumber = (level: TrainingLevel): number => {
  return TRAINING_LEVEL_ORDER.indexOf(level) + 1;
};

